// Assignment Short hands in c programming

#include<stdio.h>

int a = 25;
int b = 7;

int main ()
{
    
  printf ("a add b = %d\n", a += b); //a = a + b;
  printf ("a sub b = %d\n", a -= b); //a = a - b;
  printf ("a mul b = %d\n", a *= b); //a = a * b;
  printf ("a div b = %d\n", a /= b); //a = a / b;
  printf ("a mod b = %d\n", a %= b); //a = a % b;

}
